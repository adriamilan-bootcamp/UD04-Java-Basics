
public class Ejercicio01App {
	public static void main(String[] args) {
		
		/* 
		 * Declara dos variables numéricas (con el valor que desees),
		 * muestra por consola la suma, resta, multiplicación, división
		 * y módulo (resto de la división).
		*/
		
		// Declarando las variables
		
		int a = 4, b = 14;
		
		// Mostrando el valor de las variables
		
		System.out.println("La suma de las variables A y B es: " + (a + b));
		
		System.out.println("La resta de las variables A y B es: " + (a - b));
		
		System.out.println("La multiplicación de las variables A y B es: " + (a * b));
		
		System.out.println("La división de las variables A y B es: " + (a / b));
		
		System.out.println("El módulo de las variables A y B es: " + (a % b));
		
	}
}
